CREATE TABLE pricing_provider_result (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    asset_type VARCHAR(50) NOT NULL DEFAULT 'CARD',
    card_id BIGINT REFERENCES card(id),
    sealed_product_id BIGINT REFERENCES sealed_product(id),
    card_variant VARCHAR(80),
    provider_name VARCHAR(120) NOT NULL,
    result_type VARCHAR(80) NOT NULL,
    source_market VARCHAR(120),
    source_price NUMERIC(19, 2) NOT NULL,
    source_currency VARCHAR(3) NOT NULL,
    exchange_rate_used NUMERIC(19, 8) NOT NULL,
    price_sgd NUMERIC(19, 2) NOT NULL,
    confidence_rating VARCHAR(20) NOT NULL,
    sample_size INTEGER,
    source_url VARCHAR(1000),
    source_updated_at TIMESTAMP WITH TIME ZONE,
    provider_metadata VARCHAR(2000),
    captured_at TIMESTAMP WITH TIME ZONE NOT NULL,
    CONSTRAINT chk_pricing_provider_result_asset_reference CHECK (
        (asset_type = 'CARD' AND card_id IS NOT NULL AND sealed_product_id IS NULL)
        OR
        (asset_type = 'SEALED_PRODUCT' AND card_id IS NULL AND sealed_product_id IS NOT NULL)
    ),
    CONSTRAINT chk_pricing_provider_result_type CHECK (
        result_type IN ('RAW_CARD', 'PSA_8', 'PSA_9', 'PSA_10')
    ),
    CONSTRAINT chk_pricing_provider_result_confidence CHECK (confidence_rating IN ('HIGH', 'MEDIUM', 'LOW')),
    CONSTRAINT chk_pricing_provider_result_sample CHECK (sample_size IS NULL OR sample_size >= 0)
);

CREATE INDEX idx_price_snapshot_card_variant_time
    ON price_snapshot(card_id, card_variant, calculated_at DESC, id DESC);

CREATE INDEX idx_pricing_provider_result_card_type_time
    ON pricing_provider_result(card_id, card_variant, result_type, captured_at DESC, id DESC);
